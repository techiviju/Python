# Walrus Operator looks like a walrus with tusks,
# we mean that the := symbol resembles the shape of a walrus's long teeth!

toy=["car","dog","lion","hours","watch"]

# We can use the Walrus Operator to count and tell at the same time
if(toy_len:=len(toy))>3:
    print(f"There are {toy_len} toys in the box!")

# Walrus Operator helps you do two things at once: 
# count and check! It makes your code shorter and easier to read, 
# just like telling your friend about the toys without doing it in two steps!